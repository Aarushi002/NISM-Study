let _cache;
export function getFlashcards() {
  if (_cache) return _cache;
  const cards = [];
  let id = 0;
  for (const block of CHAPTER_THEMES) {
    for (const [front, back] of block.terms) {
      cards.push({
        id: `fc-${id++}`,
        front,
        back,
        chapter: block.ch,
        chapterLabel: `Ch.${block.ch} ${block.label}`,
        tags: ['nism-xv', `ch${block.ch}`],
      });
    }
  }
  for (const block of MORE_TERMS) {
    for (const [front, back] of block.terms) {
      cards.push({
        id: `fc-${id++}`,
        front,
        back,
        chapter: block.ch,
        chapterLabel: `Ch.${block.ch} ${block.label}`,
        tags: ['nism-xv', `ch${block.ch}`, 'advanced'],
      });
    }
  }
  cards.push(...extraCards());
  _cache = cards;
  return _cache;
}
